

import java.awt.Color;


public class Block {
int width;
int height;
Color color;
int xCord ;
int yCord ;
double yVel;


public Block(int width, int height, Color color, int xCord, int yCord, double yVel) {
	this.width = width;
	this.height = height;
	this.color = color;
	this.xCord = xCord;
	this.yCord = yCord;
	this.yVel = yVel;
	
}

}
