import java.awt.Color;

public class BottomObstacle {
	int xCord;
	int yCord;
	int height;
	int width;
	Color color;

	public BottomObstacle(int xCord, int yCord, int width, int height, Color color) {
		this.xCord = xCord;
		this.yCord = yCord;
		this.width = width;
		this.height = height;
		this.color = color;
	}
}
